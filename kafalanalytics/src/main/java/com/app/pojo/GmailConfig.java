package com.app.pojo;

public class GmailConfig {
	private String configfilelocation;
	private String serviceAccountUser;
	private String clientId;
	private boolean fileAvaialable;
	
	
	public String getConfigfilelocation() {
		return configfilelocation;
	}
	public void setConfigfilelocation(String configfilelocation) {
		this.configfilelocation = configfilelocation;
	}
	public String getServiceAccountUser() {
		return serviceAccountUser;
	}
	public void setServiceAccountUser(String serviceAccountUser) {
		this.serviceAccountUser = serviceAccountUser;
	}
	public String getClientId() {
		return clientId;
	}
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}
	public boolean isFileAvaialable() {
		return fileAvaialable;
	}
	public void setFileAvaialable(boolean fileAvaialable) {
		this.fileAvaialable = fileAvaialable;
	}
	
	
	
	

}
